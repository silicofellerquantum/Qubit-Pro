"""Palace simulation result parser.

Parses CSV output files generated by AWS Palace solvers into strongly typed
Pydantic output models.
"""

from __future__ import annotations

import csv
import logging
import re
from pathlib import Path
from typing import List

from app.services.palace.exceptions import ResultParserError
from app.services.palace.models import (
    PalaceEigenmodeMode,
    PalaceEigenmodeOutput,
    PalaceElectrostaticOutput,
    PalaceMagnetostaticOutput,
    PalaceInductanceEntry,
)

logger = logging.getLogger(__name__)


class ResultParser:
    """Parser for AWS Palace output files."""

    @staticmethod
    def parse_eigenmode(output_dir: Path, port_names: List[str] | None = None) -> PalaceEigenmodeOutput:
        """Parse eigenmode results from eig.csv and port-EPR.csv.

        Args:
            output_dir: The directory containing solver output CSV files.
            port_names: Optional ordered names of the lumped ports/junctions.

        Returns:
            A populated PalaceEigenmodeOutput model.

        Raises:
            ResultParserError: If parsing fails or required files are missing.
        """
        logger.info("Parsing eigenmode results from directory: %s", output_dir)
        eig_path = output_dir / "eig.csv"
        if not eig_path.exists():
            logger.error("Required eigenmode file eig.csv not found at: %s", eig_path)
            raise ResultParserError(f"Eigenmode frequency output file eig.csv not found at: {eig_path}")

        modes = {}
        try:
            logger.info("Opening eig.csv for reading...")
            with open(eig_path, mode="r", newline="", encoding="utf-8") as f:
                reader = csv.reader(f)
                header = [col.strip() for col in next(reader)]

                try:
                    idx_m = header.index("m")
                    # Real frequency column might contain variations of "Re{f}"
                    idx_freq = next(i for i, h in enumerate(header) if "Re{f}" in h)
                    idx_q = header.index("Q")
                except (ValueError, StopIteration) as e:
                    raise ResultParserError(f"Required headers (m, Re{{f}}, Q) not found in eig.csv: {header}") from e

                for row in reader:
                    if not row or not row[0].strip() or row[0].startswith("#"):
                        continue
                    m_val = int(float(row[idx_m]))
                    freq_val = float(row[idx_freq]) / 1.0e9  # Convert Hz to GHz
                    q_val = float(row[idx_q])

                    modes[m_val] = PalaceEigenmodeMode(
                        mode_index=m_val,
                        frequency_ghz=freq_val,
                        quality_factor=q_val,
                        epr={},
                    )
        except Exception as e:
            if isinstance(e, ResultParserError):
                raise e
            raise ResultParserError(f"Failed to read/parse eig.csv: {e}") from e

        # Attempt to parse port-EPR.csv if it exists (for Energy Participation Ratio of junctions)
        epr_path = output_dir / "port-EPR.csv"
        if epr_path.exists():
            try:
                with open(epr_path, mode="r", newline="", encoding="utf-8") as f:
                    reader = csv.reader(f)
                    header = [col.strip() for col in next(reader)]

                    idx_m = header.index("m")
                    port_cols = [(idx, h) for idx, h in enumerate(header) if idx != idx_m]

                    for row in reader:
                        if not row or not row[0].strip() or row[0].startswith("#"):
                            continue
                        m_val = int(float(row[idx_m]))

                        if m_val in modes:
                            mode_epr = {}
                            for idx, col_name in port_cols:
                                val = float(row[idx])
                                # Map col_name (e.g. "EPR[1]") to the corresponding port name in port_names
                                port_id = col_name
                                if port_names:
                                    match = re.search(r"EPR\[(\d+)\]", col_name)
                                    if match:
                                        p_idx = int(match.group(1)) - 1
                                        if 0 <= p_idx < len(port_names):
                                            port_id = port_names[p_idx]
                                mode_epr[port_id] = val
                            modes[m_val].epr = mode_epr
                logger.info("Successfully parsed Energy Participation Ratios (EPR) from port-EPR.csv")
            except Exception as e:
                logger.warning("Failed to parse EPR file %s: %s (non-fatal, continuing without EPR)", epr_path, e)

        logger.info("Eigenmode result parsing completed. Total modes found: %d", len(modes))
        return PalaceEigenmodeOutput(modes=list(modes.values()))

    @staticmethod
    def parse_electrostatic(output_dir: Path, terminal_names: List[str]) -> PalaceElectrostaticOutput:
        """Parse capacitance matrix from terminal-C.csv.

        Args:
            output_dir: The directory containing solver output CSV files.
            terminal_names: Ordered names of the terminals.

        Returns:
            A populated PalaceElectrostaticOutput model.

        Raises:
            ResultParserError: If parsing fails or required files are missing.
        """
        logger.info("Parsing electrostatic results from directory: %s", output_dir)
        c_path = output_dir / "terminal-C.csv"
        if not c_path.exists():
            logger.error("Required electrostatic file terminal-C.csv not found at: %s", c_path)
            raise ResultParserError(f"Capacitance matrix file terminal-C.csv not found at: {c_path}")

        try:
            logger.info("Opening terminal-C.csv for reading...")
            with open(c_path, mode="r", newline="", encoding="utf-8") as f:
                reader = csv.reader(f)
                header = [col.strip() for col in next(reader)]

                idx_i = header.index("i")
                val_cols = [idx for idx, h in enumerate(header) if idx != idx_i]

                # Parse rows and sort them by the index 'i' (ensures alignment)
                raw_rows = []
                for row in reader:
                    if not row or not row[0].strip() or row[0].startswith("#"):
                        continue
                    raw_rows.append(row)

                raw_rows.sort(key=lambda r: int(float(r[idx_i])))

                # Build matrix in fF (Palace output is in Farads, so multiply by 1e15)
                matrix = []
                for r in raw_rows:
                    matrix_row = []
                    for col_idx in val_cols:
                        val_farad = float(r[col_idx])
                        matrix_row.append(val_farad * 1e15)
                    matrix.append(matrix_row)

                num_terminals = len(matrix)
                # Validation check
                for idx, m_row in enumerate(matrix):
                    if len(m_row) != num_terminals:
                        raise ResultParserError(
                            f"Inconsistent capacitance matrix dimensions: row {idx+1} has "
                            f"{len(m_row)} entries, expected {num_terminals}."
                        )

                # Construct terminal IDs mapping
                names = list(terminal_names)
                if len(names) < num_terminals:
                    for i in range(len(names), num_terminals):
                        names.append(f"terminal_{i+1}")
                else:
                    names = names[:num_terminals]

                logger.info("Capacitance matrix parsed successfully. terminal_ids=%s, matrix_dimension=%dx%d", names, num_terminals, num_terminals)
                return PalaceElectrostaticOutput(
                    terminal_ids=names,
                    matrix=matrix,
                    units="fF",
                )
        except Exception as e:
            logger.exception("Failed to parse electrostatic result file.")
            if isinstance(e, ResultParserError):
                raise e
            raise ResultParserError(f"Failed to read/parse terminal-C.csv: {e}") from e

    @staticmethod
    def parse_magnetostatic(output_dir: Path, terminal_names: List[str]) -> PalaceMagnetostaticOutput:
        """Parse inductance matrix from terminal-L.csv and extract self-inductances.

        Args:
            output_dir: The directory containing solver output CSV files.
            terminal_names: Ordered names of the terminals/ports.

        Returns:
            A populated PalaceMagnetostaticOutput model.

        Raises:
            ResultParserError: If parsing fails or required files are missing.
        """
        logger.info("Parsing magnetostatic results from directory: %s", output_dir)
        l_path = output_dir / "terminal-L.csv"
        if not l_path.exists():
            logger.error("Required magnetostatic file terminal-L.csv not found at: %s", l_path)
            raise ResultParserError(f"Inductance matrix file terminal-L.csv not found at: {l_path}")

        try:
            logger.info("Opening terminal-L.csv for reading...")
            with open(l_path, mode="r", newline="", encoding="utf-8") as f:
                reader = csv.reader(f)
                header = [col.strip() for col in next(reader)]

                idx_i = header.index("i")
                val_cols = [idx for idx, h in enumerate(header) if idx != idx_i]

                # Parse rows and sort them by the index 'i' (ensures alignment)
                raw_rows = []
                for row in reader:
                    if not row or not row[0].strip() or row[0].startswith("#"):
                        continue
                    raw_rows.append(row)

                raw_rows.sort(key=lambda r: int(float(r[idx_i])))

                # Build matrix in nH (Palace output is in Henries, so multiply by 1e9)
                matrix = []
                for r in raw_rows:
                    matrix_row = []
                    for col_idx in val_cols:
                        val_henry = float(r[col_idx])
                        matrix_row.append(val_henry * 1e9)
                    matrix.append(matrix_row)

                num_terminals = len(matrix)
                # Validation check
                for idx, m_row in enumerate(matrix):
                    if len(m_row) != num_terminals:
                        raise ResultParserError(
                            f"Inconsistent inductance matrix dimensions: row {idx+1} has "
                            f"{len(m_row)} entries, expected {num_terminals}."
                        )

                # Construct terminal IDs mapping
                names = list(terminal_names)
                if len(names) < num_terminals:
                    for i in range(len(names), num_terminals):
                        names.append(f"terminal_{i+1}")
                else:
                    names = names[:num_terminals]

                # Self-inductances are on the diagonal of the inductance matrix
                inductance_data = []
                for idx, name in enumerate(names):
                    inductance_data.append(
                        PalaceInductanceEntry(
                            element_id=name,
                            inductance_nH=matrix[idx][idx]
                        )
                    )

                logger.info("Inductance matrix parsed successfully. inductance_data=%s", inductance_data)
                return PalaceMagnetostaticOutput(inductance_data=inductance_data)
        except Exception as e:
            logger.exception("Failed to parse magnetostatic result file.")
            if isinstance(e, ResultParserError):
                raise e
            raise ResultParserError(f"Failed to read/parse terminal-L.csv: {e}") from e
